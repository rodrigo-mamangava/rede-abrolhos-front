                        <h1>Pesquisadores medem impactos do aquecimento global sobre Abrolhos</h1>

                        <img class="destaque" src="image/notícias/interna noticias 01.jpg">

                        <p class="legenda">
                            Imagens das placas de colonização instaladas nos recifes rasos de Abrolhos. Foto: Fernando 
                            Moraes/JBRJ
                        </p>

                        <p>
                            Manaus, AM -- Apesar dos pesares, os maiores recifes do Atlântico Sul estão crescendo. Um 
                            estudo da Rede Abrolhos demonstra que, entre 2012 e 2014, cada metro quadrado ganhou 
                            580 gramas de carbonato de cálcio por ano. A taxa é considerada, pelos autores do estudo, 
                            intermediária entre corais que já sofreram algum nível de degradação. E entre os pesares 
                            estão os efeitos do aquecimento global, que ameaçam a existência de corais em todo o 
                            mundo.
                        </p>

                        <p>
                            “Os recifes são organismos vivos, sempre em crescimento e suportando uma biodiversidade 
                            associada”, explica o biólogo Gilberto Menezes Amado-Filho, do Jardim Botânico do Rio de 
                            Janeiro e coordenador do estudo. “Na medida em que anomalias (provocadas pelo 
                            aquecimento global) se tornam frequentes, eles param de crescer e morrem, diminuindo a 
                            diversidade e a biomassa na área”, completa.
                        </p>
                        <p>
                            É a primeira vez que pesquisadores medem o crescimento de recifes coralinos no Atlântico 
                            Sul e os dados vão servir de base para estudos sobre os efeitos das Mudanças Climáticas 
                            sobre a região. Durante os estudos, foi acompanhado o crescimento de organismos sobre 
                            placas artificiais montadas no ambiente. Os resultados foram publicados em 27 de abril, na 
                            revista científica on-line PLOS One.
                        </p>
                        <p>
                            “No Norte da Austrália, na Grande Barreira de Corais, 95% dos recifes estão sofrendo 
                            branqueamento”, destaca o pesquisador. “É como se virassem um monte de concreto sem 
                            capa viva, com menor diversidade e consequências para o ciclo de carbono e ciclos 
                            biogênicos.”.
                        </p>

                        <p>
                            Apesar dos dados obtidos pela Rede Abrolhos não serem tão alarmantes, a situação ainda 
                            sim é preocupante. Os pesquisadores verificaram um incremento de tufas de algas e micro-
                            organismos após ondas de calor registradas no verão de 2013/2014. Esses tufos formam o 
                            segundo grupo mais abundante nas placas de colonização e passaram a ocupar de 1 a 4% da 
                            área total das placas em 2012/2013 para 25% no verão seguinte.
                        </p>

                        <p>
                            “Na medida em que a temperatura aumenta, organismos que vamos chamar de 
                            oportunistas crescem sobre os formadores e os matam, sufocam os organismos 
                            construtores”, explica Gilberto Amado-Filho. Os estudos demonstram também que algas 
                            calcárias, e não corais, são os principais formadores dos recifes.
                        </p>
                        <p>
                            Os pesquisadores continuam a monitorar a região de Abrolhos, com instalação de novas 
                            placas de colonização e sensores de temperatura e qualidade da água. Apesar de parte dos 
                            recifes serem protegidas pelo Parque Nacional e outras unidades de conservação, a 
                            ampliação das áreas de proteção em Abrolhos, prometida pelo Ministério do Meio 
                            Ambiente há quase uma década, ainda está parada.
                        </p>




